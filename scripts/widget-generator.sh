    echo -e "${GREEN}[WidgetGen]${NC} $1"
}

# Generate a clock widget
generate_clock_widget() {
    local name="${1:-wehttamsnaps-clock}"
    local style="${2:-digital}"  # digital, analog, minimal
    
    log "Generating clock widget: $name (style: $style)"
    
    mkdir -p "$OUTPUT_DIR"
    
    cat > "$OUTPUT_DIR/$name.qml" << EOF
// WehttamSnaps Clock Widget
// Generated by widget-generator.sh
// Style: $style

import QtQuick
import QtQuick.Layouts
import Quickshell
import Quickshell.Widgets

Rectangle {
    id: root
    implicitWidth: 200
    implicitHeight: 60
    color: "$BASE"
    radius: 12
    
    // Gradient border effect
    Rectangle {
        anchors.fill: parent
        anchors.margins: 2
        radius: 10
        color: "transparent"
        border.width: 2
        border.color: Qt.binding(() => {
            // Animated gradient border
            let t = Date.now() / 2000
            let r = Math.sin(t) * 0.5 + 0.5
            return Qt.rgba(
                0x89/255 * (1-r) + 0xcb/255 * r,  // Blue to Purple
                0xb4/255 * (1-r) + 0xa6/255 * r,
                0xfa/255 * (1-r) + 0xf7/255 * r,
                1.0
            )
        })
    }
    
    RowLayout {
        anchors.centerIn: parent
        spacing: 10
        
        // Time display
        Text {
            id: timeText
            font.family: "JetBrains Mono"
            font.pixelSize: 24
            font.bold: true
            color: "$TEXT"
            text: Qt.formatDateTime(new Date(), "hh:mm")
        }
        
        // Separator
        Rectangle {
            width: 2
            height: 30
            color: "$MAUVE"
            radius: 1
        }
        
        // Date display
        Text {
            id: dateText
            font.family: "JetBrains Mono"
            font.pixelSize: 12
            color: "$SUBTEXT1"
            text: Qt.formatDateTime(new Date(), "ddd MMM dd")
        }
    }
    
    // Update time every second
    Timer {
        interval: 1000
        running: true
        repeat: true
        onTriggered: {
            timeText.text = Qt.formatDateTime(new Date(), "hh:mm")
            dateText.text = Qt.formatDateTime(new Date(), "ddd MMM dd")
        }
    }
}
EOF
    
    success "Created clock widget: $OUTPUT_DIR/$name.qml"
}

