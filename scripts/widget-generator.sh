# Generate a branding banner widget
generate_banner_widget() {
    local name="${1:-wehttamsnaps-banner}"
    
    log "Generating branding banner widget: $name"
    
    mkdir -p "$OUTPUT_DIR"
    
    cat > "$OUTPUT_DIR/$name.qml" << EOF
// WehttamSnaps Branding Banner Widget
// Shows brand name with animated gradient

import QtQuick
import QtQuick.Layouts

Rectangle {
    id: root
    implicitWidth: 250
    implicitHeight: 50
    color: "transparent"
    
    RowLayout {
        anchors.centerIn: parent
        spacing: 10
        
        // Camera icon
        Text {
            text: "📷"
            font.pixelSize: 24
        }
        
        // Brand name with gradient
        Text {
            text: "WehttamSnaps"
            font.family: "JetBrains Mono"
            font.pixelSize: 20
            font.bold: true
            
            // Animated color
            color: Qt.binding(() => {
                let t = Date.now() / 3000
                let i = (Math.sin(t) + 1) / 2
                return Qt.rgba(
                    0x89/255 * (1-i) + 0xcb/255 * i,
                    0xb4/255 * (1-i) + 0xa6/255 * i,
                    0xfa/255 * (1-i) + 0xf7/255 * i,
                    1.0
                )
            })
        }
        
        // Tagline
        Text {
            text: "| Photography & Gaming"
            font.family: "JetBrains Mono"
            font.pixelSize: 11
            color: "#a6adc8"
        }
    }
    
    // Animation timer
    Timer {
        interval: 50
        running: true
        repeat: true
        onTriggered: root.update()
    }
}
EOF
    
    success "Created banner widget: $OUTPUT_DIR/$name.qml"
}

# Generate all widgets
generate_all() {
    log "Generating all widgets..."
    
    generate_clock_widget
    generate_stats_widget
    generate_workspace_widget
    generate_media_widget
    generate_banner_widget
    
    success "All widgets generated in $OUTPUT_DIR"
}

# List available widget templates
list_templates() {
    echo "Available Widget Templates:"
    echo ""
    echo "  clock     - Digital clock with date display"
    echo "  stats     - System stats (CPU/RAM/GPU)"
    echo "  workspace - Workspace indicator bar"
    echo "  media     - Media player controls"
    echo "  banner    - WehttamSnaps branding banner"
    echo "  all       - Generate all widgets"
}

# =============================================================================
# Main
# =============================================================================

mkdir -p "$CONFIG_DIR"
mkdir -p "$OUTPUT_DIR"

case "${1:-help}" in
    clock)
        generate_clock_widget "$2" "$3"
        ;;
    stats)
        generate_stats_widget "$2"
        ;;
    workspace)
        generate_workspace_widget "$2"
        ;;
    media)
        generate_media_widget "$2"
        ;;
    banner)
        generate_banner_widget "$2"
        ;;
    all)
        generate_all
        ;;
    list)
        list_templates
        ;;
    help|--help|-h)
        echo "WehttamSnaps Widget Generator"
        echo ""
        echo "Usage: $0 <widget-type> [name] [options]"
        echo ""
        list_templates
        echo ""
        echo "Examples:"
        echo "  $0 clock                # Generate clock widget"
        echo "  $0 stats my-stats       # Generate stats widget with custom name"
        echo "  $0 all                  # Generate all widgets"
        ;;
    *)
        error "Unknown widget type: $1"
        echo "Use: $0 list"
        exit 1
        ;;
esac