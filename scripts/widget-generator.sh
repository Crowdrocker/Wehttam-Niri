                { name: "Files", icon: "📁", color: "$MAROON" },
                { name: "Misc", icon: "⭐", color: "$LAVENDER" }
            ]
            
            delegate: Rectangle {
                Layout.preferredWidth: 32
                Layout.preferredHeight: 28
                radius: 8
                color: index === 0 ? modelData.color : "$SURFACE0"
                
                Text {
                    anchors.centerIn: parent
                    text: modelData.icon
                    font.pixelSize: 14
                }
                
                MouseArea {
                    anchors.fill: parent
                    hoverEnabled: true
                    cursorShape: Qt.PointingHandCursor
                    
                    ToolTip.visible: containsMouse
                    ToolTip.text: modelData.name
                    ToolTip.delay: 500
                    
                    onClicked: {
                        // Switch to workspace
                        console.log("Switch to workspace " + (index + 1))
                    }
                }
            }
        }
    }
}
EOF
    
    success "Created workspace widget: $OUTPUT_DIR/$name.qml"
}

